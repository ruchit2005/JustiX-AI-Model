SAMPLE_LEGAL_LAWS = """
CONSTITUTIONAL RIGHTS AND LEGAL PROCEDURES

Article I: Right to Legal Counsel
Every defendant has the constitutional right to legal representation. An attorney must be provided if the defendant cannot afford one. Attorneys must represent their clients zealously within the bounds of the law.

Article II: Burden of Proof
In criminal cases, the prosecution bears the burden of proving guilt beyond a reasonable doubt. The defendant is presumed innocent until proven guilty. This burden never shifts to the defense.

Article III: Rules of Evidence
1. Hearsay Evidence: Statements made outside of court cannot generally be used as evidence unless they fall under specific exceptions.
2. Chain of Custody: Physical evidence must have a documented chain of custody to be admissible.
3. Relevant Evidence: Only evidence that is relevant to the case may be admitted.
4. Expert Testimony: Expert witnesses must be qualified in their field to provide opinion testimony.

Article IV: Constitutional Protections
1. Fourth Amendment: Protection against unreasonable searches and seizures. Evidence obtained through illegal searches may be inadmissible.
2. Fifth Amendment: Right against self-incrimination. Defendants cannot be compelled to testify against themselves.
3. Sixth Amendment: Right to a speedy and public trial, right to confront witnesses, right to compel witnesses.

Article V: Legal Procedures
1. Opening Statements: Attorneys present an overview of their case without arguing.
2. Direct Examination: Attorney questions their own witnesses.
3. Cross-Examination: Opposing attorney questions witnesses to challenge credibility or testimony.
4. Objections: Attorneys may object to improper questions or evidence. Common objections include:
   - Hearsay
   - Leading questions (on direct examination)
   - Speculation
   - Relevance
   - Asked and answered

Article VI: Attorney Conduct
1. Attorneys must not misrepresent facts or evidence.
2. Attorneys must not make statements unsupported by evidence.
3. Attorneys must treat the court and opposing counsel with respect.
4. Attorneys cannot knowingly present false testimony.
5. Attorneys must disclose adverse legal authority in their jurisdiction.

Article VII: GPS and Electronic Evidence
GPS data from mobile devices can be used as evidence if:
1. Proper warrant was obtained (or falls under warrant exception)
2. Chain of custody is documented
3. Data integrity can be verified
4. Expert testimony establishes reliability of GPS technology

Article VIII: Witness Testimony
1. Witnesses must have personal knowledge of facts they testify about.
2. Witness credibility can be challenged through cross-examination.
3. Prior inconsistent statements can be used to impeach witnesses.
4. Character witnesses can testify to a defendant's reputation or character traits.

Article IX: Alibi Defense
When presenting an alibi defense:
1. Defense should provide notice of alibi witnesses.
2. Alibi must account for defendant's whereabouts at time of crime.
3. Corroborating evidence strengthens alibi (receipts, video, third parties).
4. GPS data or phone records can support or contradict alibis.

Article X: Reasonable Doubt Standard
The prosecution must prove each element of the crime beyond a reasonable doubt. Reasonable doubt is:
- Not mere possible doubt
- Not beyond all doubt
- A doubt based on reason and common sense
- Doubt that would make a reasonable person hesitate to act

Article XI: Video and Photographic Evidence
Security footage and photographs are admissible if:
1. Properly authenticated (witness can verify accuracy)
2. Not misleading or altered
3. Relevant to issues in the case
4. Image quality sufficient for intended purpose (identification may require clear images)

Article XII: Closing Arguments
During closing arguments, attorneys:
1. May argue reasonable inferences from evidence
2. Cannot inject personal opinions
3. Cannot refer to facts not in evidence
4. Must not appeal to jury's bias or prejudice
5. Should summarize key evidence supporting their position

These guidelines form the foundation of legal practice and must be followed by all attorneys practicing law.
"""

